package com.tilismtech.tellotalk_shopping_sdk.pojos.requestbody;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class GenerateToken {


    @SerializedName("Grant_username")
    @Expose
    private String grantUsername;
    @SerializedName("Grant_Password")
    @Expose
    private String grantPassword;
    @SerializedName("grant_type")
    @Expose
    private String grantType;
    @SerializedName("profileId")
    @Expose
    private String profileId;
    @SerializedName("firstname")
    @Expose
    private String firstname;
    @SerializedName("middlename")
    @Expose
    private String middlename;
    @SerializedName("lastname")
    @Expose
    private String lastname;
    @SerializedName("phone")
    @Expose
    private String phone;
    @SerializedName("email")
    @Expose
    private String email;

    public String getGrantUsername() {
        return grantUsername;
    }

    public void setGrantUsername(String grantUsername) {
        this.grantUsername = grantUsername;
    }

    public String getGrantPassword() {
        return grantPassword;
    }

    public void setGrantPassword(String grantPassword) {
        this.grantPassword = grantPassword;
    }

    public String getGrantType() {
        return grantType;
    }

    public void setGrantType(String grantType) {
        this.grantType = grantType;
    }

    public String getProfileId() {
        return profileId;
    }

    public void setProfileId(String profileId) {
        this.profileId = profileId;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getMiddlename() {
        return middlename;
    }

    public void setMiddlename(String middlename) {
        this.middlename = middlename;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
